def extract_text_robust(pdf_path, ocr=True, ocr_dpi=200):
    doc = fitz.open(pdf_path)
    pages_out = []

    for pno, page in enumerate(doc, start=1):
        print(f"Processing page {pno}/{doc.page_count}...")

        try:
            txt = page.get_text("text")
        except:
            txt = ""

        if txt and len(txt.strip()) > 50:
            pages_out.append(txt.strip())
            continue

        try:
            page_dict = page.get_text("dict")
            blocks = page_dict.get("blocks", [])
        except:
            blocks = []

        blocks = sorted(blocks, key=lambda b: (b["bbox"][1], b["bbox"][0]))

        used_xrefs = set()
        page_parts = []

        for block in blocks:
            btype = block.get("type", 0)

            if btype == 0:
                t = block.get("text", "").strip()
                if t:
                    page_parts.append(t)
                    continue

            if btype == 1 and ocr:
                info = block.get("image")
                xref = info.get("xref") if isinstance(info, dict) else None

                if xref and xref not in used_xrefs:
                    used_xrefs.add(xref)
                    try:
                        pix = fitz.Pixmap(doc, xref)
                        if pix.n > 4:
                            pix = fitz.Pixmap(fitz.csRGB, pix)

                        img = Image.open(io.BytesIO(pix.tobytes("png")))
                        txt = pytesseract.image_to_string(img).strip()
                        if txt:
                            page_parts.append(txt)
                    except:
                        pass

        if not page_parts and ocr:
            try:
                pix = page.get_pixmap(dpi=ocr_dpi)
                img = Image.open(io.BytesIO(pix.tobytes("png")))
                txt = pytesseract.image_to_string(img).strip()
                if txt:
                    page_parts.append(txt)
            except:
                pass

        pages_out.append("\n".join(page_parts))

    doc.close()
    return "\n\n".join(pages_out)

