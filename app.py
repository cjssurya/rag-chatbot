def generate_answer(query):
    # Embed query
    q_embed = model.encode([query], convert_to_numpy=True).astype("float32")

    # Retrieve top-5 chunks
    D, I = index.search(q_embed, 12)

    retrieved = [clean_text(chunks[i]) for i in I[0]]
    context = "\n\n".join(retrieved).strip()

    # Prevent hallucination
    if len(context.strip()) == 0:
        return "Not available"

    prompt = f"""
    Use ONLY the context provided below to answer the question.
    If the answer is not in the context, reply exactly: Not available.
    explain in paragraphs


    Context:
    {context}

    Question: {query}

    Answer:
    """

    ans = pipe(prompt, max_new_tokens=2000)[0]["generated_text"].strip()
    return ans

